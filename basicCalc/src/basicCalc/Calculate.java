package basicCalc;

public class Calculate {
	public void calculationAdd(double inOne, double inTwo) {
		System.out.println(inOne + inTwo);
	}
	public void calculationSubtract(double inOne, double inTwo) {
		System.out.println(inOne - inTwo);
	}
	public void calculationMult(double inOne, double inTwo) {
		System.out.println(inOne * inTwo);
	}
	public void calculationDiv(double inOne, double inTwo) {
		System.out.println(inOne * inTwo);
	}
	
}
